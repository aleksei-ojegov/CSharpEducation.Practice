namespace Math
{
    public class MathHelper
    {
        public int addition(int summ1, int summ2)
        {
            return summ1 + summ2;
        }

        public int subtraction(int summ1, int summ2)
        {
            return summ1 - summ2;
        }

        public int multiplication(int summ1, int summ2)
        {
            return summ1 * summ2;
        }

        public int division(int summ1, int summ2)
        {
            return summ1 / summ2;
        }
    }
}
